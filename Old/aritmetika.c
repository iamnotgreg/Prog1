#include <stdio.h>

int main()
{
    int a = 20;
    int b = 3;
    
    printf("%d + %d = %d\n", a, b, a+b); 
    
    printf("%d - %d = %d\n", a, b, a-b);
    
    printf("%d * %d = %d\n", a, b, a*b);
    
    printf("%d / %d = %d\n", a, b, a/b);
    
    printf("%d %% %d = %d\n", a, b, a%b);
    
    return 0;
}
