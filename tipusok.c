#include <stdio.h>

int main()
{
    int szam;
    
    szam = 2024;
    
    printf("A szám értéke: %d\n", szam);
    printf("---\n");
	
	char betu;
	betu = 'L';
	printf("A karakter értéke: %c\n", betu);
	
	float pi = 3.14;
	printf("---\n");
	printf("Pí értéke %f\n", pi);
	
	double hossz = 2.5;
	printf("---\n");
	printf("hossz: %lf\n", hossz);
	
    return 0;
}
